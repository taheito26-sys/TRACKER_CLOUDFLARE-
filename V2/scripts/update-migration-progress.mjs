#!/usr/bin/env node
import fs from 'node:fs';

const TASKS_FILE = 'V2/MIGRATION_TASKS.md';
const STATUS_FILE = 'V2/MIGRATION_EXECUTION_STATUS.md';
const OUTPUT_FILE = 'V2/MIGRATION_PROGRESS.md';

function progressBar(percent, width = 24) {
  const filled = Math.round((percent / 100) * width);
  return '█'.repeat(filled) + '░'.repeat(Math.max(0, width - filled));
}

function parseTasks(md) {
  const total = (md.match(/^- \[[ x]\] /gm) || []).length;
  const done = (md.match(/^- \[x\] /gm) || []).length;
  return { total, done, percent: total ? Math.round((done / total) * 100) : 0 };
}

function updateStatusFile(content, line) {
  const start = '<!-- PROGRESS_BAR_START -->';
  const end = '<!-- PROGRESS_BAR_END -->';
  const block = `${start}\n${line}\n${end}`;
  if (content.includes(start) && content.includes(end)) {
    return content.replace(new RegExp(`${start}[\\s\\S]*?${end}`), block);
  }
  // prepend under title
  return content.replace(/^# V2 Migration Execution Status\n\n/m, `# V2 Migration Execution Status\n\n${block}\n\n`);
}

const tasks = fs.readFileSync(TASKS_FILE, 'utf8');
const stats = parseTasks(tasks);
const bar = progressBar(stats.percent);
const line = `**Progress:** ${stats.done}/${stats.total} tasks (${stats.percent}%)  \`${bar}\``;

const out = `# V2 Migration Live Progress\n\n${line}\n\n- Source: \`${TASKS_FILE}\`\n- Updated: ${new Date().toISOString()}\n\n> This file is auto-generated by \`node V2/scripts/update-migration-progress.mjs\`.\n`;

fs.writeFileSync(OUTPUT_FILE, out);

const status = fs.readFileSync(STATUS_FILE, 'utf8');
const updatedStatus = updateStatusFile(status, line);
fs.writeFileSync(STATUS_FILE, updatedStatus);

console.log(`[progress] ${stats.done}/${stats.total} (${stats.percent}%)`);
